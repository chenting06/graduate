export const countUserNutritionEnergy = function (
  userWeight: number,
  userHeight: number,
  userWorkout: String
) {
  let standardWeight: number = userHeight - 105;
  let standardCoefficient;
  let totalEnergy;
  let nutritionWeight = [];
  let nutritionEnergy = [];
  if (Math.abs(standardWeight - userWeight) > standardWeight * 0.2) {
    standardCoefficient = standardWeight > userWeight ? 40 : 30;
  } else {
    switch (userWorkout) {
      case "soft":
        standardCoefficient = 30;
        break;
      case "medium":
        standardCoefficient = 35;
        break;
      case "hard":
        standardCoefficient = 40;
        break;
    }
  }
  totalEnergy = standardCoefficient * standardWeight;
  for (let i = 0; i < 3; i++) {
    switch (i) {
      case 0:
        nutritionEnergy[i] = totalEnergy * 0.15;
        nutritionWeight[i] = (totalEnergy * 0.15) / 4;
        break;
      case 1:
        nutritionEnergy[i] = totalEnergy * 0.3;
        nutritionWeight[i] = (totalEnergy * 0.3) / 9;
        break;
      case 2:
        nutritionEnergy[i] = totalEnergy * 0.55;
        nutritionWeight[i] = (totalEnergy * 0.55) / 4;
        break;

      default:
        break;
    }
  }
  return { nutritionEnergy, nutritionWeight };
};
